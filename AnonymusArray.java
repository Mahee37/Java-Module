public class AnonymusArray {
    public static void main(String args[])
    {
        AnonymusArray(new int[]  {1,3,5,7,9});
        
    }
    public static void AnonymusArray(int[] arr)
    {
        for(int i=0;i<arr.length;i++)
        {
            System.out.print(arr[i] +" ");
        }
        System.out.println();
    }
}
