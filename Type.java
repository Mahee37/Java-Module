class Typeconversion
{
    public static  void main(String[] args)
    {
        int a = 260;
        byte b = (byte)a;

        float x = 7.3f;
        int m = (int)x;
        System.out.println(b);
       System.out.println(a);
       System.out.println(x);
        System.out.println(m);
    }
} 